package day21;

public class OddNumberPrintRepetition {


    /*
    count 1 to 10==> print only odd numbers

    repeat thie 4 time

     */
    public static void main(String[] args) {


        for (int i = 1; i <= 10; i++) {
            if (i % 2 == 1) {
                System.out.println(i);

            }
        }

    }
}